enum class MapStyle { Hellas, Kemet, Skandia, Albion, Persis, Shinar, Zhongguo, Mayab, Latium }

enum class PlaceType(val glyph: String, val label: String) {
    City("◉", "City"),
    Sanctuary("✦", "Sanctuary"),
    Temple("☗", "Temple"),
    Mountain("▲", "Peak"),
    Battle("⚔", "Battle"),
    Isle("❖", "Isle"),
}

data class PlaceStat(val label: String, val value: String)

data class HistoricCharacter(
    val id: String,
    val name: String,
    val role: String,
    val glyph: String,
    val accent: Color,
    val epithet: String,
    val might: Int,
    val wisdom: Int,
    val renown: Int,
    val cunning: Int,
    val divinity: Int,
) {
    val total: Int get() = might + wisdom + renown + cunning + divinity
}

data class HistoricPlace(
    val id: String,
    val name: String,
    val type: PlaceType,
    val relPos: Offset,
    val era: String,
    val synopsis: String,
    val story: String,
    val accent: Color,
    val stats: List<PlaceStat>,
    val characters: List<HistoricCharacter>,
)

data class AncientRealm(
    val id: String,
    val name: String,
    val tagline: String,
    val era: String,
    val accent: Color,
    val mapStyle: MapStyle,
    val places: List<HistoricPlace>,
)

// --- Character constructors (short aliases) ---
private fun hc(
    id: String, name: String, role: String, glyph: String, accent: Color, epithet: String,
    might: Int, wisdom: Int, renown: Int, cunning: Int, divinity: Int,
) = HistoricCharacter(id, name, role, glyph, accent, epithet, might, wisdom, renown, cunning, divinity)

