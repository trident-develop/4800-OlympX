    val allCharacters: List<HistoricCharacter> by lazy {
        realms.flatMap { it.places }.flatMap { it.characters }.distinctBy { it.id }.sortedBy { it.name }
    }

    fun findRealmOf(charId: String): AncientRealm? =
        realms.firstOrNull { it.places.any { p -> p.characters.any { c -> c.id == charId } } }
